package pl.nkg.iot.inode.example.ui;

import android.content.Context;
import android.os.Bundle;
import android.support.v4.app.Fragment;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ScrollView;

import pl.nkg.iot.inode.core.LogProvider;
import pl.nkg.iot.inode.example.R;
import pl.nkg.iot.inode.example.ui.views.LogView;

public class DeviceFragment extends Fragment implements LogProvider {

    private static final String BUNDLE_SAVED_LOGS = "saved";

    private OnFragmentInteractionListener mListener;
    private LogView mLogView;
    private ScrollView mScrollView;

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        View view = inflater.inflate(R.layout.fragment_device, container, false);
        mScrollView = (ScrollView) view;
        mLogView = (LogView) view.findViewById(R.id.logView);
        if (savedInstanceState != null) {
            mLogView.setText(savedInstanceState.getString(BUNDLE_SAVED_LOGS));
        }
        return view;
    }

    @Override
    public void onAttach(Context context) {
        super.onAttach(context);
        if (context instanceof OnFragmentInteractionListener) {
            mListener = (OnFragmentInteractionListener) context;
        } else {
            throw new RuntimeException(context.toString()
                    + " must implement OnFragmentInteractionListener");
        }
    }

    @Override
    public void onDetach() {
        super.onDetach();
        mListener = null;
    }

    @Override
    public void onSaveInstanceState(Bundle outState) {
        super.onSaveInstanceState(outState);
        outState.putString(BUNDLE_SAVED_LOGS, mLogView.getText().toString());
    }

    @Override
    public void println(int priority, String tag, String msg, Throwable tr) {
        mLogView.println(priority, tag, msg, tr);
        mScrollView.post(new Runnable() {
            @Override
            public void run() {
                mScrollView.fullScroll(ScrollView.FOCUS_DOWN);
            }
        });
    }

    public interface OnFragmentInteractionListener {
    }
}
